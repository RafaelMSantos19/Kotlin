fun main() {

    var idade = 30

    var idadeSum = idade +1
    var idadeSub = idade -1
    var idadeMult = idade * 2
    var idadeDiv = idade / 2

    println("Idade total: $idade")
    println("Idade somada: $idadeSum")
    println("Idade subitraida: $idadeSub")
    println("Idade Multiplicada: $idadeMult")
    println("Idade Dividida: $idadeDiv")

    var resto = idade % 2

    println("Resto da divisao: $resto")

    // Outras formas de soma variaveis
    println(idade++)
    println(idade)
    println(++idade)

}


